// 날짜 비교 함수
function isDateInRange(date, start, end) {
  if (start <= end) {
    return date >= start && date <= end;
  } else {
    return date >= start || date <= end;
  }
}

function getZodiac(month, day) {
  const mmdd = (month < 10 ? "0" + month : month) + "-" + (day < 10 ? "0" + day : day);
  for (let i = 0; i < zodiacs.length; i++) {
    const z = zodiacs[i];
    if (isDateInRange(mmdd, z.start, z.end)) {
      return z.name;
    }
  }
  return null;
}

function getRandomItem(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

document.getElementById("userForm").addEventListener("submit", function(e){
  e.preventDefault();
  const name = document.getElementById("name").value.trim();
  const birthdate = document.getElementById("birthdate").value;
  if (!name || !birthdate) return alert("이름과 생년월일을 모두 입력해주세요.");

  const bd = new Date(birthdate);
  const zodiac = getZodiac(bd.getMonth() + 1, bd.getDate());
  if (!zodiac) return alert("별자리를 찾을 수 없습니다.");

  const data = horoscopeData[zodiac];

  document.getElementById("greeting").textContent = `${name}님, 오늘의 ${zodiac} 운세입니다.`;
  document.getElementById("advice").textContent = getRandomItem(data.advice);
  document.getElementById("fun").textContent = getRandomItem(data.fun);
  document.getElementById("luck").textContent = getRandomItem(data.luck);
  document.getElementById("emotional").textContent = getRandomItem(data.emotional);

  document.getElementById("result").style.display = "block";
});
